package server.kafka;

import org.apache.kafka.clients.consumer.ConsumerConfig;
import org.apache.kafka.clients.consumer.ConsumerRecord;
import org.apache.kafka.clients.consumer.ConsumerRecords;
import org.apache.kafka.clients.consumer.KafkaConsumer;
import org.apache.kafka.common.serialization.StringDeserializer;

import java.time.Duration;
import java.util.Collections;
import java.util.Properties;

public class Consumer {

    public static void main(String[] args) {
        Properties p = new Properties();
        p.put(ConsumerConfig.BOOTSTRAP_SERVERS_CONFIG, "127.0.0.1:9092");
        p.put(ConsumerConfig.KEY_DESERIALIZER_CLASS_CONFIG, StringDeserializer.class);
        p.put(ConsumerConfig.VALUE_DESERIALIZER_CLASS_CONFIG, StringDeserializer.class);
        p.put(ConsumerConfig.GROUP_ID_CONFIG, "migoo.test");

        try (KafkaConsumer<String, String> kafkaConsumer = new KafkaConsumer<>(p)) {
            kafkaConsumer.subscribe(Collections.singletonList("migoo.topic"));// 订阅消息
            while (true) {
                ConsumerRecords<String, String> records = kafkaConsumer.poll(Duration.ofMinutes(1L));
                for (ConsumerRecord<String, String> record : records) {
                    System.out.printf("topic:%s,offset:%d,消息:%s%n", record.topic(), record.offset(), record.value());
                    System.out.println();
                }
            }
        }
    }
}